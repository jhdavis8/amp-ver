int* create_perm_array(int n, int* counts, int* perm_array, int a, int indx, int count){
	
  for (int i = a; i < n; i++){
    if(counts[i] > 1 && count == 0){
      perm_array[i] += i;
      indx++;
      count++;
      counts[i] -= 1;
      create_perm_array(n, counts, perm_array, a, indx, count);
    }
    else if(counts[i] > 1 && count != 0){
      perm_array[count] += i;
      indx++;
      count++;
      counts[i] -= 1;
      create_perm_array(n, counts, perm_array, a, indx, count);
    }
    else if(counts[i] <= 1 && count > 0){
      perm_array[count] = i;
      indx++;
      counts[i] += count;
      count=0;
      create_perm_array(n, counts, perm_array, (a+1), indx, count);
    }
    else {
      perm_array[indx] += i;
      indx++;
    }
  }
  return perm_array;
}

void perm_aux(int **table,
	      int i0,
	      int j0,
	      int len,
	      int * counts) {
  for (int i=0; i<len; i++) {
    if (counts[i] != 0) {
      int new_counts[len];
      for (int j=0; j<len; j++) {
	new_counts[j] = j==i ? counts[j]-1 : counts[j];
      }
      int np = calc_num_perms(len, new_counts);
      for (int j=0; j<np; j++) {
	table[i0+j][j0] = i;
      }
      perm_aux(table, i0, j0+1, len, new_counts);
      i0 += np;
    }
  }
}

/* Linearizability check, returns false if the next op to be added has a start time
   that is earlier than some other op that has already been added to the permuation.*/
_Bool check_times(int* row,
		  int curr_total_op_count,
		  int tid_next,
		  int** start_times,
		  int nthreads) {
  int curr_op_counts[nthreads];
  int counts_index[nthreads];
  for (int i = 0; i < curr_total_op_count - 1; i++) {
    curr_op_counts[row[i]]++;
  }
  int next_start_time = start_times[tid_next][curr_op_counts[tid_next]];
  for (int i = 0; i < curr_total_op_count - 1; i++) {
    if (next_start_time < start_times[row[i]][curr_op_counts[counts_index[row[i]]++]]) {
      return false;
    }
  }
  return true;
}


/* Analogous to perm_aux but includes linearizability check */
void linearizable_aux(int **table,
		      int i0,
		      int j0,
		      int len,
		      int * counts,
		      int ** start_times,
		      int nthreads) {
  for (int i=0; i<len; i++) {
    if (counts[i] != 0) {
      int new_counts[len];
      for (int j=0; j<len; j++) {
	new_counts[j] = j==i ? counts[j]-1 : counts[j];
      }
      int np = calc_num_perms(len, new_counts);
      for (int j=0; j<np; j++) {
	if (table[i0+j][0] >= 0) {
	  if (check_times(table[i0+j], j0, i, start_times, nthreads)) {
	    table[i0+j][j0] = i;
	  } else {
	    table[i0+j][0] = -1;
	  }
	}
      }
      perm_aux(table, i0, j0+1, len, new_counts);
      i0 += np;
    }
  }  
}


/* Computes the permutations generated by counts array */
int** compute_perms(int n, int* counts){
  int num_rows = calc_num_perms(n, counts);
  int sum = 0;
  for (int i = 0; i < n; i++){
    sum += counts[i];
  }
  int num_cols = sum;
  int** result = malloc(num_rows * sizeof(int*));
  for (int i = 0; i < num_rows; i++){
    result[i] = malloc(num_cols * sizeof(int));
  }
  perm_aux(result, 0, 0, n, counts);
  return result;
}

/* Computes the permutations generated by counts array, filtering out
   permuations that fail to meet the linearizability condition. Filtered
   perms are flagged with a -1 in their first index. */
int** compute_linearizable_perms(int n, int* counts, int** start_times) {
  int num_rows = calc_num_perms(n, counts);
  int sum = 0;
  for (int i = 0; i < n; i++) {
    sum += counts[i];
  }
  int num_cols = sum;
  int** result = malloc(num_rows * sizeof(int*));
  for (int i = 0; i < num_rows; i++){
    result[i] = malloc(num_cols * sizeof(int));
  }
  linearizable_aux(result, 0, 0, n, counts, start_times, n);
  return result;
}


