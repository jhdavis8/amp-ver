/*  Factorial(n) */
int fact(int n){
  int result = 1;
	
  for (int i = 1; i <= n; i++){
    result *= i;
  }
  return result;
}

/* Computes the number of permutations generated by the counts array */
int calc_num_perms(int n, int * counts){
  int sum = 0;
	
  for (int i = 0; i < n; i++){
    sum += counts[i];
  }
  int numerator = fact(sum);
	
  int denom = 1;
  for (int i = 0; i < n; i++){
    denom *= fact(counts[i]);
  }
	
  return numerator/denom;
}

